mysets1 = {1,4,5,8,7,0,}
mysets2 = {7,8,9,10,1,2,3,4}
print(mysets1)
print(mysets2)
print("\n")

#union sets
print(mysets1 | mysets2)
print("\n")

#intersection sets
print(mysets1 & mysets2)
print("\n")

#difference sets
print(mysets1 - mysets2)
print(mysets2 - mysets1)
print("\n")


