mydict = {1:"America",2:"Canada",3:"Dubai"}

#Dictionary Methods

#Accessing Dictionary
print(mydict[3])
print("\n")

#Len Dictionary
print(len(mydict))
print("\n")

#Key Dictionary
print(mydict.keys())
print("\n")

#values Dictionary
print(mydict.values())
print("\n")

#item Dictionary
print(mydict.items())
print("\n")

#get Dictionary
print(mydict.get(2))
print("\n")

#update Dictionary
mydict.update({4:"Norway"})
print(mydict)
print("\n")

#pop Dictionary
mydict.pop(1)
print(mydict)
print("\n")
